import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import nextEnv from "@next/env";

const { loadEnvConfig } = nextEnv;

const projectDir = process.cwd();
loadEnvConfig(projectDir);

const outputDir = path.join(projectDir, "src", "generated");
const serverEnvOutputPath = path.join(outputDir, "serverEnv.ts");
const authEnvOutputPath = path.join(outputDir, "authEnv.ts");

const envKeys = [
  "AUTH_SECRET",
  "NEXTAUTH_SECRET",
  "AUTH_URL",
  "NEXTAUTH_URL",
  "AUTH_TRUST_HOST",
  "GOOGLE_CLIENT_ID",
  "AUTH_GOOGLE_ID",
  "GOOGLE_CLIENT_SECRET",
  "AUTH_GOOGLE_SECRET",
  "OVERRIDE_EMAIL",
  "GOOGLE_SERVICE_ACCOUNT_TYPE",
  "GOOGLE_SERVICE_ACCOUNT_PROJECT_ID",
  "GOOGLE_SERVICE_ACCOUNT_PRIVATE_KEY_ID",
  "GOOGLE_SERVICE_ACCOUNT_PRIVATE_KEY",
  "GOOGLE_SERVICE_ACCOUNT_CLIENT_EMAIL",
  "GOOGLE_SERVICE_ACCOUNT_CLIENT_ID",
  "GOOGLE_DRIVE_CLIENT_ID",
  "GOOGLE_DRIVE_CLIENT_SECRET",
  "GOOGLE_DRIVE_REFRESH_TOKEN",
  "GOOGLE_DRIVE_REDIRECT_URI",
  "R2_ACCOUNT_ID",
  "R2_ACCESS_KEY_ID",
  "R2_SECRET_ACCESS_KEY",
  "R2_BUCKET",
  "R2_PUBLIC_BASE_URL",
  "SHEET_NAME_ENTRADAS",
  "NEXT_PUBLIC_SPREADSHEET_ID",
];

const authEnvKeys = [
  "AUTH_SECRET",
  "NEXTAUTH_SECRET",
  "AUTH_URL",
  "NEXTAUTH_URL",
  "AUTH_TRUST_HOST",
  "GOOGLE_CLIENT_ID",
  "AUTH_GOOGLE_ID",
  "GOOGLE_CLIENT_SECRET",
  "AUTH_GOOGLE_SECRET",
  "OVERRIDE_EMAIL",
];

const serializeValue = (value) => {
  if (typeof value !== "string" || value.length === 0) {
    return "undefined";
  }

  return JSON.stringify(value);
};

const buildContent = (exportName, keys) => `// This file is auto-generated by scripts/generate-server-env.mjs.
// Do not edit it manually.
export const ${exportName} = {
${keys.map((key) => `  ${key}: ${serializeValue(process.env[key])},`).join("\n")}
} as const;
`;

await mkdir(outputDir, { recursive: true });
await writeFile(serverEnvOutputPath, buildContent("SERVER_ENV", envKeys), "utf8");
await writeFile(authEnvOutputPath, buildContent("AUTH_ENV", authEnvKeys), "utf8");

const availableKeys = envKeys.filter((key) => {
  const value = process.env[key];
  return typeof value === "string" && value.length > 0;
}).length;

console.log("[server-env] Generated src/generated/serverEnv.ts and src/generated/authEnv.ts", {
  availableKeys,
  totalKeys: envKeys.length,
});